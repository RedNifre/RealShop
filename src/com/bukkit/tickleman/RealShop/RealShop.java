package com.bukkit.tickleman.RealShop;

public class RealShop
{

	public String world;
	public Integer posX;
	public Integer posY;
	public Integer posZ;
	public String player;

	public RealShop(String world, Integer posX, Integer posY, Integer posZ, String player)
	{
		this.world = world;
		this.posX = posX;
		this.posY = posY;
		this.posZ = posZ;
		this.player = player;
	}

}
